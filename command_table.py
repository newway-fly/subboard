# command_table.py
# ============================================================
# 字符串命令参考表
# 用于 PC / HostBoard 与从机(SubBoard)通信
# 
# 通信模式说明：
# 1. USB直接通信: PC ↔ 从机(SubBoard) 直接通信
# 2. UART透传通信: PC → HostBoard → 从机(SubBoard)
#     - 主机转发命令格式: "S<id> <command>" (例如: "S1 ADC ALL")
#     - 从机返回格式: "S<id> <response>" (例如: "S1 ADC 1 DC_Power = 2048")
# ============================================================

# -----------------------------------------------------------------
# ADC 读取命令
# -----------------------------------------------------------------
# ADC 1                  -> 读取通道1 ADC（按索引，1-based）
# ADC ALL                -> 读取所有 ADC 通道
# ADC "DC_Power"         -> 按名称读取 ADC 通道
#
# 返回格式（每通道一行）:
# ADC <idx> <name> = <value>
# 示例: ADC 1 DC_Power = 2048
# -----------------------------------------------------------------

# -----------------------------------------------------------------
# DAC 设置/读取命令
# -----------------------------------------------------------------
# DAC 1 1023             -> 设置 DAC1 为 1023（0-4095）
# DAC "DAC_Parm" 2048    -> 按名称设置 DAC 通道
# DAC ALL 1023           -> 设置所有 DAC 通道为相同值
# DAC 1                  -> 读取 DAC1 当前值
# DAC "DAC_Parm"         -> 按名称读取 DAC 通道
# DAC ALL                -> 读取所有 DAC 通道
#
# 返回格式:
# DAC <idx> <name> = <value>
# 示例: DAC 1 DAC_Parm = 2048
# -----------------------------------------------------------------

# -----------------------------------------------------------------
# GPIO 控制命令
# -----------------------------------------------------------------
# GPIO "HostMode_EnVBus" "HIGH"  -> 设置指定引脚为高电平
# GPIO "HostMode_EnVBus" "LOW"   -> 设置指定引脚为低电平
# GPIO "HostMode_EnVBus"         -> 读取指定引脚状态
# GPIO                           -> 读取所有 GPIO 引脚状态
#
# 支持的有效引脚名称（根据config.py中的GPIO_CONFIG）:
# - "HostMode_EnVBus"
# - "USRSW" (只读，输入模式)
# - "Pam4_MPD_Sel_Res"
# - "Seq_In"
# - "Seq_Out" (只读，输入模式)
#
# 返回格式:
# SET操作: GPIO SET <pin_name> <level>
# READ操作: GPIO <pin_name> : <value> , <mode>
# -----------------------------------------------------------------

# -----------------------------------------------------------------
# SWEEP 扫描命令（从机专用功能）
# -----------------------------------------------------------------
# SWEEP <start> <stop> <points> <delay> [mode] [fixed_arm] [vref] [volt_bias_amp]
# 示例: SWEEP 1000 2000 50 100 1 620 3.382 5.0
# 示例: SWEEP 1000 2000 50 100 3      (使用默认参数)
# 示例: SWEEP STOP                     (停止扫描)
#
# 参数说明（[]内为可选参数，不提供时使用默认值）:
#   start        : 扫描起始值 (DAC值: 0-4095)
#   stop         : 扫描结束值 (DAC值: 0-4095)
#   points       : 扫描点数 (1-250，超过250自动限制为250)
#   delay        : 每步延迟时间(ms) (最小35ms，低于此值自动设为35)
#   mode         : 扫描模式 (可选，默认1)
#                 1: DAC_Parm 扫描，DAC_Narm 固定
#                 2: DAC_Narm 扫描，DAC_Parm 固定
#                 3: DAC_Parm 扫描，DAC_Narm 公式计算
#                 4: DAC_Parm 扫描，DAC_Narm 按ADC公式计算
#   fixed_arm    : 固定臂DAC值 (可选，默认620)
#   vref         : MCU参考电压(V) (可选，默认3.382)
#   volt_bias_amp: 偏置电压放大倍数 (可选，默认5.0)
#
# 数据返回格式（每步实时返回）:
# SWEEP DATA <step_idx> <dac_parm> <dac_narm> <ADC_V_Parm> <ADC_I_Parm> 
#            <ADC_V_Narm> <ADC_I_Narm> <DC_Power> <Pam4_MPD_Sel_Res>
#
# 开始/结束提示:
# SWEEP START, For DEBUG
# SWEEP END, Times: <扫描步数>
# -----------------------------------------------------------------

# -----------------------------------------------------------------
# 系统状态命令
# -----------------------------------------------------------------
# 当前工程没有专门的系统状态命令
# 可通过ADC ALL读取所有模拟输入状态
# 可通过GPIO读取所有数字输入输出状态
# -----------------------------------------------------------------

# -----------------------------------------------------------------
# 扩展命令框架
# -----------------------------------------------------------------
# EXT <func_name> [args...] -> 调用扩展函数
# 需要在 state_machine.py 的 ext_func_dict 中注册函数后才能使用
# -----------------------------------------------------------------

# ============================================================
# 使用示例
# ============================================================
# USB 直接通信 (PC -> 从机):
#   发送: "ADC ALL"
#   返回: "ADC 1 DC_Power = 2048\r\nADC 2 AC_Lock = 1024\r\n..."
#
# UART 透传通信 (PC -> HostBoard -> 从机):
#   PC发送: "S1 ADC ALL" (通过HostBoard转发)
#   从机返回: "S1 ADC 1 DC_Power = 2048\r\nS1 ADC 2 AC_Lock = 1024\r\n..."
#
# 多从机控制:
#   PC发送: "S1 DAC 1 2048"  -> 设置从机1的DAC1
#   PC发送: "S2 DAC 1 1024"  -> 设置从机2的DAC1
#
# SWEEP 扫描（实时返回数据）:
#   发送: "SWEEP 1000 3000 100 50 1 620"
#   返回: "SWEEP START, For DEBUG"
#         "SWEEP DATA 0 1000 620 ..."
#         "SWEEP DATA 1 1100 620 ..."
#         ...
#         "SWEEP END, Times: 100"
# ============================================================

# ============================================================
# 注意事项
# ============================================================
# 1. 命令不区分大小写，但建议使用大写保持一致性
# 2. 所有命令以换行符结束（\n 或 \r\n）
# 3. 多个命令可用换行符分隔，一次性发送
# 4. SWEEP扫描期间，其他命令仍可正常响应
# 5. 从机ID范围：1-4（根据config.py中的SLAVE_IDS）
# 6. 响应中的\r\n是MicroPython默认换行符
# ============================================================